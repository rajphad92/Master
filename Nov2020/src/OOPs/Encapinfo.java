package OOPs;

public class Encapinfo {
	
	// Declaration of variable as private
	private String name;
	private String id;
	private int age;
	
	public String getName() {
		return name;
	}
	public String getId() {
		return id;
	}
	public int getAge() {
		return age;
	}
	public void setName(String Name1) {
		name = Name1;
	}
	public void setId(String id) {
		this.id = id;
	}
	public void setAge(int age) {
		this.age = age;
	}
	
}
