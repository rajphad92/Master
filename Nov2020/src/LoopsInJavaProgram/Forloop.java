package LoopsInJavaProgram;

public class Forloop {

	public static void main(String[] args) {
		int i;
		//for (Declaration+initialization;  Condition; increment/Decrement)
		for (i=1; i<10; i++) {
			System.out.println(i);
		}
		for (int k=10; k>0; k--) {
			System.out.println(k);
		}
	}
}



