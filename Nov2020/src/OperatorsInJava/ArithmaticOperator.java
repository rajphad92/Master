package OperatorsInJava;

public class ArithmaticOperator {
	
	
	public static void main(String[] args) {
		
		int num1 = 100;
		int num2 = 30;
		System.out.println("Number1 + Number2 = "+ (num1+num2)); // 100+30 = 130
		System.out.println("Number1 - Number2 = "+ (num1-num2)); // 100-30 = 70
		System.out.println("Number1 * Number2 = "+ (num1*num2)); // 100*30 = 3000
		System.out.println("Number1 / Number2 = "+ (num1/num2)); // 100/30 = 3
		System.out.println("Number1 % Number2 = "+ (num1*num2)); // 100%30 = 
	}
}
