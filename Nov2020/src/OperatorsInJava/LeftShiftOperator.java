package OperatorsInJava;

public class LeftShiftOperator {

	public static void main(String[] args) {
		
		int u = 20;
		System.out.println(u<<2); //  20 * 2^2 = 20 *4 = 80
		System.out.println(u<<3); //  20 * 2^3 = 20 *8 = 160
		System.out.println(u<<4); //  20 * 2^4 = 20 *16 = 320
	}
}
